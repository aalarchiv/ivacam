        {#if gen}
          <table>
            <tbody>
              <tr><th>{t('report.stats.objects')}</th><td>{gen.stats.object_count}</td></tr>
              <tr><th>{t('report.stats.offsets')}</th><td>{gen.stats.offset_count}</td></tr>
              <tr><th>{t('report.stats.segments')}</th><td>{gen.toolpath.length}</td></tr>
              <tr><th>{t('report.stats.enabled_ops')}</th><td>{enabledOps.length}</td></tr>
            </tbody>
          </table>
        {:else}
          <p class="rb-empty">{t('report.stats.empty')}</p>
        {/if}
      </section>

      {#if timeEst}
