<script lang="ts">
  /// Printable project-summary report. Single-column layout that prints
  /// cleanly via window.print() — operators can hand-off the printout
  /// with the cut. Pulls every value from data the frontend already has
  /// (project state + project.gen.generated). No new backend computation;
  /// deeper analyses (material removed, per-op time breakdown) deferred
  /// to a follow-up — the bd issue notes 4-5 days of work, this MVP ships
  /// the user-visible 80%.
  import Modal from './Modal.svelte';
  import { project, type OpEntry } from '../state/project.svelte';
  import { isProgramOnlyOp } from '../state/op_types';
  import type { TimeEstimate } from '../api/types';
  import { buildReportMarkdown, type ReportData } from './report-markdown';
  import { saveReportMarkdown } from '../services/file_ops';
  import { isAndroid } from '../api/env';
  import { t } from '../i18n';

  /// Full build identity for the "Generated by" line + Markdown front
  /// matter. Package semver + git-describe + build date, from the vite
  /// defines (see vite.config.ts).
  const pkgVersion = typeof __IVAC_PKG_VERSION__ === 'string' ? __IVAC_PKG_VERSION__ : '0.0.0';
  const buildVersion =
    typeof __IVAC_BUILD_VERSION__ === 'string' ? __IVAC_BUILD_VERSION__ : 'unknown';
  const buildDate = typeof __IVAC_BUILD_DATE__ === 'string' ? __IVAC_BUILD_DATE__ : '';
  const generatedBy =
    `ivaCAM v${pkgVersion}` +
    (buildVersion && buildVersion !== 'unknown' ? ` (${buildVersion})` : '') +
    (buildDate ? `, built ${buildDate}` : '');

  // Local HH:MM:SS formatter — mirrors the one in GenerateBar but kept
  // self-contained so the report stays printable in isolation.
  function formatHms(s: number | undefined): string {
    if (!s || !isFinite(s) || s < 0) return '00:00:00';
    const total = Math.round(s);
    const h = Math.floor(total / 3600);
    const m = Math.floor((total % 3600) / 60);
    const sec = total % 60;
    return [h, m, sec].map((n) => String(n).padStart(2, '0')).join(':');
  }

  interface Props {
    open: boolean;
    onClose: () => void;
  }
  let { open, onClose }: Props = $props();

  const now = new Date();
  const date = $derived(now.toLocaleString());

  const gen = $derived(project.gen.generated);
  const timeEst = $derived<TimeEstimate | null>(
    (gen as { time_estimate?: TimeEstimate } | null)?.time_estimate ?? null,
  );

  const enabledOps = $derived(project.data.operations.filter((o) => o.enabled));
  /// Distinct tool IDs referenced by enabled ops, excluding Pause
  /// (which carries toolId 0 but uses no tool).
  const usedToolIds = $derived(
    Array.from(new Set(enabledOps.filter((o) => o.kind !== 'pause').map((o) => o.toolId))),
  );
  const usedTools = $derived(
    usedToolIds
      .map((id) => project.data.tools.find((t) => t.id === id))
      .filter((t): t is NonNullable<typeof t> => t != null),
  );

  function opSourceSummary(op: OpEntry): string {
    if (op.kind === 'pause') return '—';
    if (op.sourceObjects && op.sourceObjects.length > 0) {
      return t('report.source.objects', { n: op.sourceObjects.length });
    }
    if (op.sourceLayers === null) return t('report.source.all_geometry');
    if (op.sourceLayers.length === 0) return t('report.source.no_layers');
    return op.sourceLayers.map((l) => `"${l}"`).join(', ');
  }

  function opDepth(op: OpEntry): string {
    if (op.kind === 'pause') return '—';
    if ('depth' in op && op.depth != null) {
      return `${op.depth.toFixed(2)} mm`;
    }
    return '—';
  }

  function opStatusLabel(op: OpEntry): string {
    if (op.kind === 'pause') return t('report.status.pause');
    if (!gen) return t('report.status.not_generated');
    const w = (gen.warnings ?? []).filter((x) => x.op_id === op.id);
    if (w.some((x) => x.kind === 'tool_kind_mismatch' || x.kind === 'tool_geometry_impossible')) {
      return t('report.status.error');
    }
    if (w.length > 0) return t('report.status.warnings', { n: w.length });
    return t('report.status.ok');
  }

  const warningsBySeverity = $derived.by(() => {
    const all = gen?.warnings ?? [];
    const bad = all.filter(
      (w) => w.kind === 'tool_kind_mismatch' || w.kind === 'tool_geometry_impossible',
    );
    return { bad: bad.length, warn: all.length - bad.length, top: all.slice(0, 5) };
  });

  function printNow() {
    window.print();
  }

  /// Assemble the report into the pure ReportData shape, then serialize
  /// to Markdown + front matter and save.
  function reportData(): ReportData {
    const toolNameFor = (id: number) =>
      project.data.tools.find((t) => t.id === id)?.name ?? `#${id}`;
    return {
      projectName: projectName(),
      generatedBy,
      dateIso: now.toISOString(),
      dateDisplay: date,
      stats: gen
        ? {
            objects: gen.stats.object_count,
            offsets: gen.stats.offset_count,
            segments: gen.toolpath.length,
            enabledOps: enabledOps.length,
          }
        : null,
      estimatedTime: timeEst ? formatHms(timeEst.total_s) : null,
      tools: usedTools.map((t) => ({ name: t.name, kind: t.kind, diameterMm: t.diameter })),
      ops: enabledOps.map((op) => ({
        name: op.name,
        kind: op.kind,
        // Every program-only kind (Pause, Homing, Probe, CycleMarker,
        // GcodeInclude) is tool-less by design.
        tool: isProgramOnlyOp(op.kind) ? '—' : toolNameFor(op.toolId),
        source: opSourceSummary(op),
        depth: opDepth(op),
        status: opStatusLabel(op),
      })),
      warnings: warningsBySeverity,
    };
  }

  async function saveMarkdown() {
    const base = projectName().replace(/\.[^.]+$/, '') || 'report';
    await saveReportMarkdown(buildReportMarkdown(reportData()), base);
  }

  function projectName(): string {
    const src = project.transformedImport;
    if (src?.filename) return src.filename;
    if (project.activeProjectPath) {
      return project.activeProjectPath.split(/[\\/]/).pop() ?? t('report.untitled_project');
    }
    return t('report.untitled_project');
  }
</script>

{#if open}
  <Modal
    {onClose}
    modalClass="report-modal"
    persistKey="report"
    width="min(640px, 96vw)"
    draggable
    resizable
    ariaLabelledBy="report-title"
  >
    <header class="report-head">
      <h2 id="report-title">{t('report.title')}</h2>
      <button type="button" class="dlg-close" onclick={onClose} aria-label={t('common.close')}
        >×</button
      >
    </header>
    <article class="report-body" id="report-print-root">
      <header class="rb-meta">
        <h1>{projectName()}</h1>
        <p class="rb-sub">{date} · {t('report.brand_subtitle')}</p>
      </header>

      <section>
