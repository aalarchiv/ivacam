              <tr><th>{t('report.time.cut')}</th><td>{formatHms(timeEst.cut_s)}</td></tr>
              <tr><th>{t('report.time.rapid')}</th><td>{formatHms(timeEst.rapid_s)}</td></tr>
              <tr><th>{t('report.time.plunge')}</th><td>{formatHms(timeEst.plunge_s)}</td></tr>
              <tr><th>{t('report.time.retract')}</th><td>{formatHms(timeEst.retract_s)}</td></tr>
              <tr><th>{t('report.time.arc')}</th><td>{formatHms(timeEst.arc_s)}</td></tr>
              <tr
                ><th>{t('report.time.tool_change')}</th><td>{formatHms(timeEst.toolchange_s)}</td
                ></tr
              >
              <tr
                ><th>{t('report.time.spindle_warmup')}</th><td
                  >{formatHms(timeEst.spindle_warmup_s)}</td
                ></tr
              >
            </tbody>
          </table>
        </section>
      {/if}

      <section>
        <h3>{t('report.tools.title')}</h3>
        {#if usedTools.length === 0}
          <p class="rb-empty">{t('report.tools.empty')}</p>
        {:else}
          <table>
            <thead>
              <tr
                ><th>#</th><th>{t('report.tools.col.name')}</th><th
                  >{t('report.tools.col.diameter')}</th
                ><th>{t('report.tools.col.kind')}</th><th>{t('report.tools.col.feed')}</th><th
                  >{t('report.tools.col.speed')}</th
                ></tr
              >
            </thead>
            <tbody>
              {#each usedTools as t (t.id)}
                <tr>
                  <td>{t.id}</td>
                  <td>{t.name}</td>
                  <td>{t.diameter.toFixed(2)}</td>
                  <td>{t.kind}</td>
                  <td>{t.feedRate ?? '—'}</td>
                  <td>{t.speed ?? '—'}</td>
                </tr>
              {/each}
            </tbody>
          </table>
        {/if}
      </section>

      <section>
        <h3>{t('report.ops.title')}</h3>
        {#if enabledOps.length === 0}
          <p class="rb-empty">{t('report.ops.empty')}</p>
        {:else}
          <table>
            <thead>
              <tr>
                <th>#</th><th>{t('report.ops.col.name')}</th><th>{t('report.ops.col.kind')}</th><th
                  >{t('report.ops.col.tool')}</th
                >
                <th>{t('report.ops.col.source')}</th><th>{t('report.ops.col.depth')}</th><th
                  >{t('report.ops.col.status')}</th
                >
              </tr>
            </thead>
            <tbody>
              {#each enabledOps as op (op.id)}
                <tr>
                  <td>{op.id}</td>
                  <td>{op.name}</td>
                  <td>{op.kind}</td>
                  <td>
                    <!-- Program-only ops show a dash, not the
                         missing-tool sentinel. -->
                    {isProgramOnlyOp(op.kind)
                      ? '—'
                      : (project.data.tools.find((t) => t.id === op.toolId)?.name ??
                        `#${op.toolId}`)}
                  </td>
                  <td>{opSourceSummary(op)}</td>
                  <td>{opDepth(op)}</td>
                  <td>{opStatusLabel(op)}</td>
                </tr>
              {/each}
            </tbody>
          </table>
        {/if}
      </section>

      {#if gen && warningsBySeverity.bad + warningsBySeverity.warn > 0}
        <section>
          <h3>{t('report.warnings.title')}</h3>
          <p>
            {t('report.warnings.summary', {
              critical: warningsBySeverity.bad,
              nonCritical: warningsBySeverity.warn,
            })}
          </p>
          {#if warningsBySeverity.top.length > 0}
            <ul class="rb-warn-list">
              {#each warningsBySeverity.top as w, i (i)}
                <li><strong>{w.kind}</strong> — {w.message}</li>
              {/each}
            </ul>
          {/if}
        </section>
      {/if}

      <footer class="rb-foot">
        <p>{t('report.generated_by', { by: generatedBy, date })}</p>
      </footer>
    </article>
    <!-- Dialog-level action footer — direct Modal child so the floating
         sticky-footer rule pins it to the bottom. Print + Save styled
         like ok/cancel on the other modals. -->
    <footer class="report-foot">
      <button type="button" class="btn-secondary" onclick={saveMarkdown}
        >{t('report.save_markdown')}</button
      >
      <!-- Print relies on window.print(), which the Android System WebView
           doesn't implement (no system print dialog). Hide it there; Save
           Markdown is the export path on Android. -->
      {#if !isAndroid()}
        <button type="button" class="btn-primary" onclick={printNow}>{t('report.print')}</button>
      {/if}
    </footer>
  </Modal>
{/if}

